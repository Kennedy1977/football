import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const packageJsonPath = path.resolve(rootDir, "package.json");
const outputPath = path.resolve(rootDir, "apps/web/src/lib/build-meta.ts");
const outputVersionJsonPath = path.resolve(rootDir, "apps/web/public/version.json");

const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
// Package version is the single source of truth.
const version = String(packageJson.version || process.env.APP_VERSION || "1.0.0").replace(/^v/i, "");
const timestamp = String(readBuildTimestamp());
const cacheBust = `${version.replace(/\./g, "-")}-${timestamp}`;

const file = `// Auto-generated by scripts/generate-build-meta.mjs
export const APP_VERSION = "${version}" as const;
export const APP_VERSION_LABEL = "v${version}" as const;
export const APP_BUILD_TIMESTAMP = "${timestamp}" as const;
export const APP_CACHE_BUST = "${cacheBust}" as const;
`;

const json = {
  version,
  label: `v${version}`,
  buildTimestamp: timestamp,
  cacheBust,
  generatedAt: new Date(Number(timestamp)).toISOString(),
};

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, file, "utf8");
fs.mkdirSync(path.dirname(outputVersionJsonPath), { recursive: true });
fs.writeFileSync(outputVersionJsonPath, `${JSON.stringify(json, null, 2)}\n`, "utf8");

console.log(`Generated build meta: ${cacheBust}`);
console.log(`Generated runtime version file: ${path.relative(rootDir, outputVersionJsonPath)}`);

function readBuildTimestamp() {
  const raw = process.env.BUILD_TIMESTAMP;
  if (!raw) {
    return Date.now();
  }

  const parsed = Number(raw);
  return Number.isFinite(parsed) && parsed > 0 ? Math.floor(parsed) : Date.now();
}
