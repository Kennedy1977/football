import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const packageJsonPath = path.resolve(rootDir, "package.json");
const outputPath = path.resolve(rootDir, "apps/web/src/lib/build-meta.ts");

const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
const version = String(process.env.APP_VERSION || packageJson.version || "1.0.0").replace(/^v/i, "");
const timestamp = String(process.env.BUILD_TIMESTAMP || Date.now());
const cacheBust = `${version.replace(/\./g, "-")}-${timestamp}`;

const file = `// Auto-generated by scripts/generate-build-meta.mjs
export const APP_VERSION = "${version}" as const;
export const APP_VERSION_LABEL = "v${version}" as const;
export const APP_BUILD_TIMESTAMP = "${timestamp}" as const;
export const APP_CACHE_BUST = "${cacheBust}" as const;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, file, "utf8");
console.log(`Generated build meta: ${cacheBust}`);
